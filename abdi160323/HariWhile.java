package abdi160323;

import java.util.*;

public class HariWhile {
    public static void main (String[] args){
        String days [] = {"Senin","Selasa","Rabu","Kamis","Kamis","Jum'at","Sabtu","Minggu"};
        int i=0;
        while(i<days.length){
            System.out.println(days[i]);
            i++;
        }
    }
}
